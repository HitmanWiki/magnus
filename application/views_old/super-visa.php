									<p>
Parents and grandparents of Canadian citizens and permanent residents intending to obtain temporary residence to visit their close relatives may apply for extended visitors’ visas known as Super Visas. The maximum validity date for the multiple entries Super Visa is ten years, or one month prior to the applicant’s passport’s expiry, whichever is earlier. Within that time, Super Visa holders can remain in Canada for periods of up to 2-years. In comparison, a regular visitor visa is usually valid for a maximum period of 6-months.</p>
<p>
For individuals who are citizens of visa exempt countries, the Super Visa program can still be useful. Persons from visa exempt countries are ordinarily permitted to enter Canada for 6-months without obtaining a visitor visa. Under the new program, a parent or grandparent of a Canadian citizen or permanent resident from a visa exempt country can apply, from outside of Canada, for a Letter of Introduction, which they can then present to a border official upon arrival to Canada. When presented with a Letter of Introduction, the Canadian border official will normally allow the individual to enter Canada for a period of 2-years.</p>
<p>
Another advantage of Parents and Grandparents Super Visa is its relatively short processing time of approximately 8-weeks.</p>


