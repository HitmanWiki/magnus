					<p>After obtaining a study permit, students may need to renew or change the study permit during the course of their studies in Canada.</p>
					<p>If a student at a post-secondary institution wishes to change the institution, study program, or level of study, he or she does not need to apply for a new study permit. However, he or she is required to update IRCC upon changing institutions. For example, a student may move from Bachelor level to Master's level, or from Geography to Philosophy, or from a college to a university, without applying for a new study permit. He or she does not need to apply for a change to the condition of the study permit. A student may study in Canada as long as the study permit is valid.</p>
					<p>However, an international student moving from elementary school to high school, or from high school to a post-secondary institution, is required to apply for a change in the conditions of the study permit. The application must be submitted to the IRCC Case Processing Centre in Vegreville, Alberta.</p>
