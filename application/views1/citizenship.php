		<p>On June 21, 2017, the Canadian government passed An Act to amend the Citizenship Act and to make consequential amendments to another Act, SC 2017, c 14, bringing about significant changes to Canada’s citizenship revocation laws.</p>

<p>As a result Citizenship Revocation proceedings no longer apply to dual citizens living in Canada who have been convicted of treason, spying, and terrorism offenses. Instead, officials must utilize the Canadian criminal justice system.</p>

<p>The Minister may, however, still revoke citizenship on the grounds that the person obtained, retained, renounced, or resumed citizenship by false representation, fraud, or knowingly concealing material circumstances. But as per the Federal Court decision in Hassouna v. Minister of Citizenship and Immigration, such an action demands an opportunity to be heard through an oral hearing on the basis of fairness. As such, this new law has repealed the previous administrative revocation process introduced in 2014 and replaced it with a new process that includes an automatic entitlement to a hearing before the Federal Court.</p>

<p>Provisions of Bill C-6 involving changes to the citizenship revocation process came into effect on February 5, 2018. Any individual whose case is subject to possible revocation now has the choice to request that the Minister decide, or to have their case heard and decided by the Federal Court. The revocation process will also now include an additional step, where Immigration, Refugees and Citizenship officials review case submissions and decide whether or not to continue to proceed with revocation before it is referred to the Federal Court for decision. Individuals who request to have the Minister decide, may seek leave to judicially review the Minister’s decision at the Federal Court.</p>

<p>Magnus can help you respond to and appeal your citizenship revocation. We have experience handling such cases, and will assist you by communicating with Citizenship Officials, representing you to the Minister or before a Federal Court judge, and defending your interests.</p>

<p>Magnus will ensure you receive effective and strong representation. We will examine the details of your case to determine whether the decision was unreasonable or whether there was a breach of procedural fairness. We will advance strong grounds of appeal to the Minister or before the Court, arguing that your case should be sent back for re-determination by a different Citizenship official.</p>

<p>To discuss how we can assist you with appealing your citizenship revocation, please contact our office to set up a consultation to speak to one of our immigration and refugee lawyers.</p>
	

