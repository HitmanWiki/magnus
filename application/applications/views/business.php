		<p>Foreign investor entrepreneurs who want to relocate to Canada with their immediate families, can buy an established business or launch a new business in Canada. Then, qualified investors can apply for a work visa as a management level employee under the federal Temporary Foreign Worker (TFW) program.  Revised rules governing new business owners under the TFW program is gaining considerable local and international attention.</p>

<p>After a period of less than 1-year, successful temporary work visa holders and their families can change to permanent residence under a provincial immigration program or as a federal skilled worker under Express Entry.</p>

<p>The purchase of the business by the foreign national must be supported by a suitable business plan that will, along with other conditions, result in the creation or retention of Canadian jobs.</p>

<h5>WHO QUALIFIES?</h5>
<p>A foreign investor with verifiable, transferable management experience.
Sufficient assets to purchase, or start the targeted business in Canada.
Sufficient language abilities in either English or French to actively work as a manager in the business.</p>

<h5>WHAT IS THE PROCESS?</h5>
<p>The foreign investor identifies a Canadian business to purchase.
A Labour Market Impact Assessment (LMIA) is submitted along with a suitable business plan.
Once a positive LMIA opinion is issued, the foreign investor applies for a 12-24 months, renewable temporary work permit.</p>

<h5>HOW LONG DOES THIS PROCESS TAKE?</h5>
<p>Once a suitable business is found, it will take 2-3 months to complete the LMIA application process.  It will take less than 3-months in most jurisdictions, to receive a work permit.</p>

<h5>When can the work permit holder apply for Canadian permanent residence?</h5>
<p>There is no minimum wait time.  Qualified candidates may apply for permanent residence with their families, soon after they arrive in Canada.</p>

<h5>IS THIS A FORMAL GOVERNMENT PROGRAM?</h5>
<p>No.  The above described process relies on the rules governing owner-operator policies, and other immigration programs to achieve the above described outcome.</p>

<h5>WHO IS AN IDEAL CANDIDATE FOR A “BUY BUSINESS AND WORK IN CANADA VISA”?</h5>
<p>Opportunities under formal investment-based immigration programs are very limited in Canada.  Most provincial investment-based residence immigration programs operate by an invitation to apply scheme.  That overall process takes 12-36 months and often longer to conclude.</p>

<p>A foreign investor entrepreneur, motivated to live in a particular area of Canada, can purchase a suitable business and relocate to that area of choice.  This is far more advantageous than trying to meet provincial immigration programs.</p>

<p>With the exception of the Quebec Immigrant Investor program, provincial investment-based immigration programs are becoming less attractive to foreign investor entrepreneurs.</p>



