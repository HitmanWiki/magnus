<h5 class="pt-5">WHAT IS THE RURAL AND NORTHERN IMMIGRATION PILOT?</h5>
<p>The Rural and Northern Immigration Pilot is community-driven, which means participating communities take the lead in attracting new immigrants and matching them with local job vacancies, promoting a welcoming community, and connecting newcomers to established members of the community and its settlement services.</p>

<h6>COMMUNITY AND ECONOMIC DEVELOPMENT ORGANIZATION ROLES AND RESPONSIBILITIES</h6>
<p>Communities must work with a local economic development organization to fulfil the pilot's eligibility criteria.</p>

<p>Selected communities and local economic development organizations must:</p>
<ul class="txt-list">
	<li>
organize key players within the community, including employers, to identify opportunities where immigration can best support the local economy and share the importance and value of immigration with the community;</li>
<li>promote the pilot and the community to eligible foreign nationals;
recruit and assess suitable candidates for immigration by considering the economic needs of the community and an employment opportunity for the candidate;</li>
<li>prepare the community for the arrival of new immigrants by working with partners that provide services and develop strong support networks for new immigrants;</li>
<li>promote the integration of new immigrants in the community by connecting newcomers with established members of the community and settlement/social services (i.e. housing, education, transportation, and health care);</li>
<li>monitor and report on the results of the pilot in the community.</li>
</ul>


