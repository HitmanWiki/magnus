                                  <p>CIC HAS NOW ANNOUNCED CHANGES TO THE CURRENT CAREGIVER PROGRAM REGIME.  THE NEW CAREGIVER PROGRAM IS DETAILED BELOW.</p>
<h5>END TO THE LIVE-IN REQUIREMENT</h5>
<p>The new Caregiver Program will no longer require that caregivers live-in with their employers.
 The requirement for caregivers to live in the home of their employer has traditionally placed them
  in vulnerable situations, including uncompensated overtime, poor working conditions, or worse,
   until they are able to achieve permanent residence status.</p>

<p>A live-in arrangement is still possible if the employer and caregiver both agree to this 
arrangement. In these cases, the Labour Market Impact Assessment ("LMIA") submitted by the employer
 will need to include an assessment of the living arrangements and employers will no longer be
  able to deduct room and board from the caregiver's wages.</p>

<h5>TWO PATHWAYS TO PERMANENT RESIDENCE:</h5>
<p>The new Caregiver Program will no longer require that caregivers live-in with their employers. 
The requirement for caregivers to live in the home of their employer has traditionally placed them 
in vulnerable situations, including uncompensated overtime, poor working conditions, or worse, until 
they are able to achieve permanent residence status.</p>
<p>
A live-in arrangement is still possible if the employer and caregiver both agree to this arrangement. 
In these cases, the Labour Market Impact Assessment ("LMIA") submitted by the employer will need to
 include an assessment of the living arrangements and employers will no longer be able to deduct room 
 and board from the caregiver's wages.</p>

<h5>TWO PATHWAYS TO PERMANENT RESIDENCE:</h5>
<p>On November 30, 2014, two separate pathways to permanent residence will be introduced for 
caregivers. Both pathways will be assessed within 6 months of application and will have a 
yearly cap of 2,750 applications each, for a total of 5,500 applications
 (this number does not include spouses and dependants). CIC aims to admit a total of 17,500 
 caregivers in the 2014 year and another 30,000 caregivers in 2015. These numbers both include
  spouses and dependants of caregivers.</p>

<p>The two permanent residence regimes are described in detail below:</p>
<ol>
	<li>
<b>Caring for Children Pathway</b>
<p>
Qualifications for the "Caring for Children" permanent residence pathway includes:</p>
<ul class="txt-list">
<li>two years of full-time work experience in Canada as a home child care provider within the past four years;</li>

<li>a minimum language requirement of "initial intermediate" by meeting Canadian Language Benchmark 5 in a designated third-party language test; and</li>

<li>a Canadian post-secondary education credential of at least one year, or an equivalent foreign credential supported by an Educational Credential Assessment.</li>
</ul>
</li>
<li>
<b>Caring for People with High Medical Needs Pathway</b>
<p>Qualifications for "Caring for People with High Medical Needs" permanent residence pathway includes:</p>
<ul class="txt-list">
<li>
two years of full-time work experience in Canada providing in-home care or care in a health facility to the elderly or persons with disabilities or chronic disease as (ie. a registered nurse,a registered psychiatric nurse, a licensed practical nurse, a nurse aide, a patient service associate or a home support worker);</li>
<li>
demonstrating that they are licensed to practice in Canada, if applicable;
a minimum language requirement of "intermediate" by meeting Canadian Language Benchmark 7 in a designated third-party language test, if applying as a registered nurse or registered psychiatric nurse;<p>
a minimum language requirement of "initial intermediate" by meeting Canadian Language Benchmark 5 in a designated third-party language test, if applying in any other qualifying occupation; and</p>
</li>
<li>
a Canadian post-secondary education credential of at least one year, or an equivalent foreign credential supported by an Educational Credential Assessment.
</li>
</ul>
</li>
</ol>
