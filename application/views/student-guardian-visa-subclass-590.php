<p><b>Australia Student Guardian Visa Subclass 590</b> is a visa program that allows applicant to live in Australia as a Legal Guardian of an International Student who is less than 18 years of age  studying in Australia on a Student visa Subclass 500. The applicant can stay in Australia on the temporary basis till the time the course of a student is not completed and have to take care and fulfill the Student’s requirements. For being able to apply for the visa, the candidates have to meet the Student Guardian visa Checklist Australia .</p>
<br>
<h4>Student Guardian Visa Subclass 590 requirements</h4>
<p>
Candidates who are in list to to be Guardian of an International Student have to fulfill the below-given eligibility requirements. Candidates:</p>
<ol>
	<li>
Be at least 21 years of age and should have legal custody of the student or should be in a relation with the student.</li>
<li>The Guardian can’t bring any other family member during their stay in Australia.</li>
<li>Have to be financially strong to  provide accommodation, general welfare and other necessities to the student.</li>
<li>Have to qualify the health and character requirements.</li>
<li>Can stay in Australia on temporary basis.</li>
</ol>
<br>
<h4>Student Guardian Visa Subclass 590 application process:</h4>
<p>Candidates must submit all the required documents mentioned in the document checklist to support the claims made in the visa application. You can check the below-mentioned Student Guardian Visa checklist to avoid the delay process.</p>
<br>
<h4>Australian Student Guardian Visa Subclass 590 checklist:</h4>
<p><b>1) Documents:</b> If the documents are not available in English language, translated documents by accessing authority must be submitted.</p>

<p><b>2) Financial requirement:</b> You must be financially strong to support you, the student, and the accompanying member with you for the duration of your stay in Australia.</p>

<p>You have to provide evidence of your financial status by providing any one of the following:</p>
<ol>
	<li>
You have to show proof of funds stating that you have sufficient amount of money  to live in Australia.</li>
<li>Evidence that your spouse (who is not traveling to Australia) will support you and your dependents and have an annual income of at least AUD 70,000.</li>
</ol>
