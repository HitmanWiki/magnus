<p><b>3) Health Insurance : </b>

You need to provide proof of your adequate health insurance for the first 12 months of your stay in Australia. This can be obtain through Overseas Visitors Health Cover (OVHC) or another health insurance product from Australia or your home country that provides a similar level of cover.</p>
<p> <b>4) Annual Income Option </b>
The annual income requirement is AUD70, 000 or more. The income must be one of the following:</p>
<p>The personal income of your spouse</p>
<p>In the form of official government documentation such as a tax assessment (or country equivalent).</p>

<p><b>5) Identity and Relationship documents</b>

You must provide identity documents and proof of your relationship with the student. This can include a birth certificate showing the names of both parents.</p>

<p>You must also provide:</p>
<ol>
<li>A birth certificate showing the names of both parents</li>
<li>The pages of each applicant’s current passport showing photo, personal details and passport issue and expiry dates</li>
<li>Proof of change of name, if applicable.</li>
</ol>
<p><b>6) Access to funds</b></p>

<p>You must have genuine access to funds.</p>

<p>Funds to support you and your family members must come from an acceptable source such as:</p>
<ol>
<li>Money deposits with a financial institution</li>
<li>Financial institution or government loans</li>
<li>Scholarships or sponsorships.</li>
</ol>
<p>
While assessing your application, the final decision whether the funds shown will be genuinely available by taking into account factors, such as:</p>
<ol>
<li>The nature of the relationship between you and the person providing the funds, where applicable</li>
<li>Your income, assets and employment or that of the person who is providing the funds</li>
<li>Your previous visa history and that of the person providing the funds. </li>
</ol>
<br>
