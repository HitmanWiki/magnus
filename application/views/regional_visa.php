<p>The visa validity period is 5 years. Conditions, namely 8579 will be imposed which will enforce the government’s intentions that visa holders live, work and study only in regional areas and if employer sponsored, only in the nominated position. Visa holders can move between regional areas. Regional areas are defined as any area excluding Sydney, Melbourne, Brisbane, the Gold Coast and Perth.</p>

<p>Holders of the new provisional visas will also be unable to apply for most other skills based visas in Australia unless they have completed at least three years in a designated regional area, unless exceptional circumstances exist. Additionally, similar to current arrangements for the subclass 457 and 482 visa program, holders of the new provisional visas can be negatively impacted by sponsor non-compliance.</p>

<p>The new 491 visa permanent pathway will be:
	<ul class="txt-list">
		<li>Subclass 191 Permanent Residence (Skilled Regional): To meet the requirements of the permanent visa (from November 2022) applicants must have held a subclass 491 or 494 visa for at least 3 years, have complied with the conditions on that visa and have met minimum taxable income requirements. You must show earnings of at least $53,900 per annum for three years. Your partner can also be the main applicant for the 191 visa if they can demonstrate this criteria.</li>
	</ul>
</p>

<h4 class="pt-4 pb-2 darkblue-color">Effect on current 489 visa to 887 visa pathway:</h4>
<p>The Skilled Regional (Provisional) Visa (subclass 489) is now closed to new applicants. Transitional arrangements are put in place for applications which have been lodged and are undecided, as well as applicants whose pathway currently targets the permanent Skilled Regional Visa (subclass 887).</p>

<h4 class="pt-4 pb-2 darkblue-color">Skilled Migration Changes:</h4>
<p>The skilled migration points test will change from November 16, 2019. The changes will affect subclass 491 applicants as well as applications for other skilled migration visas that have not been assessed at that time (but only in a way that is beneficial for applicants, by providing additional points). Other points for age, qualifications, overseas and Australian work experience, Australian study and English points will apply. The new points details are:</p>
<ul class="txt-list">
<li>15 points for nomination by a State or Territory government agency or sponsorship by a family member residing in regional Australia, to live and work in regional Australia;</li>
<li>10 points for certain Science, Technology, Engineering and Mathematics (STEM) qualifications;</li>
</ul>
<h5 class="pt-4 pb-2">Partner Points</h5>
<p>10 points, if one of the following apply:</p>
<ul class="txt-list">
<li>skilled spouse or de facto partner (Skills assessment and Competent English); or</li>
<li>applicants without a spouse or de facto partner (single) or</li>
</ul>
<p class="pt-4">5 points,</p>
<ul class="txt-list">
<li>if your partner has Competent English. Your partner can be over 45 years of age and does not need a Skills Assessment</li>
</ul>

<h5 class="pt-4 pb-2">Summary of the 491 visa:</h5>
<ul class="txt-list">
<li>14,000 places allocated each year</li>
<li>504 eligible occupations (77 more than closest non-regional equivalent visa).</li>
<li>Replaces 489 family and state sponsored visas</li>
<li>Must be sponsored by an eligible relative or state authority
Points tested</li>
<li>Visa will be granted for initially 5 years</li>
<li>Must live and work for 3 years in regional areas to get Permanent Residence (Skilled Regional) 191 Visa</li>
<li>You or your partner will need to earn for those 3 years a minimum income each year ($53,900 per annum)- for the State sponsored stream it may be in any occupation (not yet confirmed) however if employer sponsored it must be in the nominated occupation.</li>
<li>Regional Australia is everywhere except Sydney, Melbourne, Perth, Brisbane, Gold Coast.</li>
<li>You can move between regions- Regional areas may include Newcastle, Wollongong, NSW Central Coast, Adelaide, Hobart and Canberra.</li>
<li>Cannot apply for any other skilled PR or 820 partner visa for 3 years</li>
<li>DHA Fee (main applicant) $4,045 and for spouse is: $2,025</li>
</ul>

<h5 class="pt-4 pb-2">Benefits of the 491 visa:</h5>
<ul class="txt-list">
